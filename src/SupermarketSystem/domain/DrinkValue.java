package SupermarketSystem.domain;

public class DrinkValue {

    private double volume;
    private double calorficValue;
    private double carbohydrates;
    private double sugar;
    private double alcoholContent;

    public DrinkValue(double volume, double calorficValue, double carbohydrates, double sugar, double alcoholContent) {
        this.volume = volume;
        this. calorficValue = calorficValue;
        this.carbohydrates = carbohydrates;
        this.sugar = sugar;
        this.alcoholContent = alcoholContent;
    }
}
